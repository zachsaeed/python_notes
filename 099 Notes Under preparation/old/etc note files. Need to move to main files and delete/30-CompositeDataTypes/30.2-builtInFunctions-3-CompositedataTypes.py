# Composite Data Type built-in methods

# mainly used for data type creation or typecasting

# -- Sequences:
# str()  Constructs a string object
# list()	Constructs a list object
# tuple()	Creates a tuple object
# bytes()	Creates and returns a bytes object (similar to bytearray, but immutable)
# bytearray()	Creates and returns an object of the bytearray class
# range()  # Creates a range view object (We call it a view object as it doesnt store all the range values. Infact it
# generates each value dynamically when iteration is run. It also takes very little memory no matter ow bg the range
# because of this)

# -- Collections
# dict()	Creates a dict object
# set()	Creates a set object
# frozenset()	Creates a frozenset object

# object()	Returns a new featureless object

